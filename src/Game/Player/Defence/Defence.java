package Game.Player.Defence;

public class Defence {

    private int defenceDefendPoints;

    public Defence(){};

    public Defence(int dDefendPoints) {
        this.defenceDefendPoints = dDefendPoints;
    }

    public double getDefenceDefendPoints() {
        return defenceDefendPoints;
    }

    public void setDefenceDefendPoints(int defenceDefendPoints) {
        this.defenceDefendPoints = defenceDefendPoints;
    }
}
